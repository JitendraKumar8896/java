 class SimpleInterest {
   public static void main(String [] args){
    float p, r, t, si;
    p = 23000; r = 12; t = 2;
    si = (p*r*t)/100;

    System.out.println("p="+p);
    System.out.println("r="+r);
    System.out.println("t="+t);
    System.out.println("Simple Interest is: " +si);

   }
}
